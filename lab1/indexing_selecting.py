import pandas as pd
df = pd.read_csv("train_1.csv")

# üìå 1. –í–∏–±—Ä–∞—Ç–∏ –¥–µ–∫—ñ–ª—å–∫–∞ –∫–æ–ª–æ–Ω–æ–∫ —ñ —Ä—è–¥–∫—ñ–≤
selected_columns = df[["Store", "Date", "Sales"]].head(10)  # –ü–µ—Ä—à—ñ 10 —Ä—è–¥–∫—ñ–≤ —ñ–∑ –∫–æ–ª–æ–Ω–∫–∞–º–∏ Store, Date, Sales
print("üîπ –í–∏–±—Ä–∞–Ω—ñ –∫–æ–ª–æ–Ω–∫–∏ —Ç–∞ —Ä—è–¥–∫–∏:\n", selected_columns)

# üìå 2. –í–∏–±—Ä–∞—Ç–∏ –¥–µ–∫—ñ–ª—å–∫–∞ –∫–æ–ª–æ–Ω–æ–∫ —ñ —Ä—è–¥–∫—ñ–≤ –∑–∞ –¥–æ–ø–æ–º–æ–≥–æ—é .loc (–ø–æ —ñ–º–µ–Ω—ñ)
selected_loc = df.loc[0:5, ["Store", "Sales", "Customers"]]  # –ü–µ—Ä—à—ñ 6 —Ä—è–¥–∫—ñ–≤ (–≤—ñ–¥ 0 –¥–æ 5) –∑–∞ –Ω–∞–∑–≤–∞–º–∏ –∫–æ–ª–æ–Ω–æ–∫
print("üîπ –í–∏–±—ñ—Ä–∫–∞ –∑–∞ .loc:\n", selected_loc)

# üìå 3. –í–∏–±—Ä–∞—Ç–∏ –¥–µ–∫—ñ–ª—å–∫–∞ –∫–æ–ª–æ–Ω–æ–∫ —ñ —Ä—è–¥–∫—ñ–≤ –∑–∞ –¥–æ–ø–æ–º–æ–≥–æ—é .iloc (–ø–æ —ñ–Ω–¥–µ–∫—Å—É)
selected_iloc = df.iloc[0:5, [0, 3, 4]]  # –ü–µ—Ä—à—ñ 5 —Ä—è–¥–∫—ñ–≤ (–≤—ñ–¥ 0 –¥–æ 4) —ñ –∫–æ–ª–æ–Ω–∫–∏ 0, 3, 4
print("üîπ –í–∏–±—ñ—Ä–∫–∞ –∑–∞ .iloc:\n", selected_iloc)

# üìå 4. –í–∏–¥–∞–ª–∏—Ç–∏ –Ω–µ–ø–æ—Ç—Ä—ñ–±–Ω—ñ –∫–æ–ª–æ–Ω–∫–∏ (–º–µ—Ç–æ–¥ drop)
df_dropped = df.drop(columns=["StateHoliday"])  # –í–∏–¥–∞–ª—è—î–º–æ –∫–æ–ª–æ–Ω–∫—É StateHoliday
print("üîπ –¢–∞–±–ª–∏—Ü—è –ø—ñ—Å–ª—è –≤–∏–¥–∞–ª–µ–Ω–Ω—è –∫–æ–ª–æ–Ω–∫–∏:\n", df_dropped.head())

# üìå 5. –í–∏–¥–∞–ª–∏—Ç–∏ –Ω–µ–ø–æ—Ç—Ä—ñ–±–Ω—ñ —Ä—è–¥–∫–∏ (–Ω–∞–ø—Ä–∏–∫–ª–∞–¥, –ø–µ—Ä—à—ñ 2)
df_dropped_rows = df.drop(index=[0, 1])
print("üîπ –¢–∞–±–ª–∏—Ü—è –ø—ñ—Å–ª—è –≤–∏–¥–∞–ª–µ–Ω–Ω—è —Ä—è–¥–∫—ñ–≤ 0 —Ç–∞ 1:\n", df_dropped_rows.head())

# üìå 6. –í–∏–∫–æ—Ä–∏—Å—Ç–∞–Ω–Ω—è conditional selection (–≤–∏–±—ñ—Ä–∫–∞ —Ä—è–¥–∫—ñ–≤ –∑–∞ —É–º–æ–≤–æ—é)
filtered_df = df[df["Sales"] > 5000]  # –í–∏–±–∏—Ä–∞—î–º–æ —Ä—è–¥–∫–∏, –¥–µ Sales > 5000
print("üîπ –†—è–¥–∫–∏ –∑ –ø—Ä–æ–¥–∞–∂–∞–º–∏ –±—ñ–ª—å—à–µ 5000:\n", filtered_df.head())

# üìå 7. –í–∏–∫–æ—Ä–∏—Å—Ç–∞–Ω–Ω—è set_index() (–∑–º—ñ–Ω–∞ —ñ–Ω–¥–µ–∫—Å—É)
df_indexed = df.set_index("Date")  # –†–æ–±–∏–º–æ –∫–æ–ª–æ–Ω–∫—É Date —ñ–Ω–¥–µ–∫—Å–æ–º
print("üîπ –¢–∞–±–ª–∏—Ü—è –∑ –Ω–æ–≤–∏–º —ñ–Ω–¥–µ–∫—Å–æ–º (Date):\n", df_indexed.head())