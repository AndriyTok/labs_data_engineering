import pandas as pd
df = pd.read_csv("train_1.csv")

# üìå 1. –ú–µ—Ç–æ–¥ value_counts() - –ø—ñ–¥—Ä–∞—Ö—É–Ω–æ–∫ —É–Ω—ñ–∫–∞–ª—å–Ω–∏—Ö –∑–Ω–∞—á–µ–Ω—å —É –∫–æ–ª–æ–Ω—Ü—ñ
store_counts = df["Store"].value_counts()  # –ü—ñ–¥—Ä–∞—Ö—É–Ω–æ–∫ –∫—ñ–ª—å–∫–æ—Å—Ç—ñ –∫–æ–∂–Ω–æ–≥–æ –º–∞–≥–∞–∑–∏–Ω—É
print("üîπ –ö—ñ–ª—å–∫—ñ—Å—Ç—å –∑–∞–ø–∏—Å—ñ–≤ –¥–ª—è –∫–æ–∂–Ω–æ–≥–æ –º–∞–≥–∞–∑–∏–Ω—É:\n", store_counts.head())

# üìå 2. –ú–µ—Ç–æ–¥ unique() - –æ—Ç—Ä–∏–º–∞—Ç–∏ —É–Ω—ñ–∫–∞–ª—å–Ω—ñ –∑–Ω–∞—á–µ–Ω–Ω—è
unique_days = df["DayOfWeek"].unique()  # –£–Ω—ñ–∫–∞–ª—å–Ω—ñ –¥–Ω—ñ —Ç–∏–∂–Ω—è
print("üîπ –£–Ω—ñ–∫–∞–ª—å–Ω—ñ –¥–Ω—ñ —Ç–∏–∂–Ω—è:\n", unique_days)

# üìå 3. –ú–µ—Ç–æ–¥ sum() - –∑–∞–≥–∞–ª—å–Ω–∞ —Å—É–º–∞ –ø–æ –∫–æ–ª–æ–Ω—Ü—ñ
total_sales = df["Sales"].sum()  # –°—É–º–∞ –≤—Å—ñ—Ö –ø—Ä–æ–¥–∞–∂—ñ–≤
print("üîπ –ó–∞–≥–∞–ª—å–Ω—ñ –ø—Ä–æ–¥–∞–∂—ñ:", total_sales)

# üìå 4. –ú–µ—Ç–æ–¥ mean() - —Å–µ—Ä–µ–¥–Ω—î –∑–Ω–∞—á–µ–Ω–Ω—è –ø–æ –∫–æ–ª–æ–Ω—Ü—ñ
average_customers = df["Customers"].mean()  # –°–µ—Ä–µ–¥–Ω—è –∫—ñ–ª—å–∫—ñ—Å—Ç—å –ø–æ–∫—É–ø—Ü—ñ–≤
print("üîπ –°–µ—Ä–µ–¥–Ω—è –∫—ñ–ª—å–∫—ñ—Å—Ç—å –ø–æ–∫—É–ø—Ü—ñ–≤:", average_customers)

# üìå 5. –ú–µ—Ç–æ–¥ map() - –∑–º—ñ–Ω—é—î–º–æ –∑–Ω–∞—á–µ–Ω–Ω—è —É –∫–æ–ª–æ–Ω—Ü—ñ
# –ü—Ä–∏–ø—É—Å—Ç–∏–º–æ, —â–æ –∫–æ–ª–æ–Ω–∫–∞ "Open" –º–∞—î –∑–Ω–∞—á–µ–Ω–Ω—è 1 —Ç–∞ 0, –∑–∞–º—ñ–Ω–∏–º–æ —ó—Ö –Ω–∞ "–í—ñ–¥–∫—Ä–∏—Ç–æ" —Ç–∞ "–ó–∞–∫—Ä–∏—Ç–æ"
df["Open_Status"] = df["Open"].map({1: "–í—ñ–¥–∫—Ä–∏—Ç–æ", 0: "–ó–∞–∫—Ä–∏—Ç–æ"})
print("üîπ –ü–µ—Ä–µ—Ç–≤–æ—Ä–µ–Ω–Ω—è –∑–Ω–∞—á–µ–Ω—å —É –∫–æ–ª–æ–Ω—Ü—ñ Open:\n", df[["Open", "Open_Status"]].head())

# üìå 6. –ú–µ—Ç–æ–¥ map() –∑ lambda –≤–∏—Ä–∞–∑–æ–º - –∑–º—ñ–Ω–∞ –∑–Ω–∞—á–µ–Ω—å —á–µ—Ä–µ–∑ —Ñ—É–Ω–∫—Ü—ñ—é
# –ü–µ—Ä–µ–≤–µ–¥–µ–º–æ "Customers" —É –∫–∞—Ç–µ–≥–æ—Ä—ñ—é: –±—ñ–ª—å—à–µ 1000 - "–ë–∞–≥–∞—Ç–æCustomers", –º–µ–Ω—à–µ - "–ú–∞–ª–æ"
df["Customer_Category"] = df["Customers"].map(lambda x: "–ë–∞–≥–∞—Ç–æ" if x > 1000 else "–ú–∞–ª–æ")
print("üîπ –ö–∞—Ç–µ–≥–æ—Ä–∏–∑–∞—Ü—ñ—è –ø–æ–∫—É–ø—Ü—ñ–≤:\n", df[["Customers", "Customer_Category"]].head())